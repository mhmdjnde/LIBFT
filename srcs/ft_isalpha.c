#include"libft.h"

int	ft_isalpha(int c)
{
	int	i;

	i = 0;
	if (!(c >= 'a' && c <= 'z'))
	{
		if (!(c >= 'A' && c <= 'Z'))
		{
			return (0);
		}
	}
	i++;
	return (1);
}
